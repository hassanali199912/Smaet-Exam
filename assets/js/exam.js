function save_answer() {
    inputs_array.forEach(element=>{
        if (element.checked) {
            user_answer_array[quection_counter] = element.value;
        }
    });
    unchecked_inputs();
}

function unchecked_inputs() {
    inputs_array.forEach(element=>{
        if (element.checked) {
         element.checked = false;
        }
    });
}

